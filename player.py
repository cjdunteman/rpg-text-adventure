# Player Class                                                                  
class Player():                                                                 
    def __init__(self, name, level, xp, health, weapon, gold, wins, losses):    
        self.name = name
        self.level = level
        self.xp = xp
        self.health = health
        self.weapon = weapon
        self.gold = gold
        self.wins = wins
        self.losses = losses